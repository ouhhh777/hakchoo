package front.vo;

public class ContactVO {

	private String contact_code;
	private String contact_user_email;
	private String contact_type;
	private String contact_content;
	private String contact_date;
	private String contact_confirm;
	public String getContact_code() {
		return contact_code;
	}
	public void setContact_code(String contact_code) {
		this.contact_code = contact_code;
	}
	public String getContact_user_email() {
		return contact_user_email;
	}
	public void setContact_user_email(String contact_user_email) {
		this.contact_user_email = contact_user_email;
	}
	public String getContact_type() {
		return contact_type;
	}
	public void setContact_type(String contact_type) {
		this.contact_type = contact_type;
	}
	public String getContact_content() {
		return contact_content;
	}
	public void setContact_content(String contact_content) {
		this.contact_content = contact_content;
	}
	public String getContact_date() {
		return contact_date;
	}
	public void setContact_date(String contact_date) {
		this.contact_date = contact_date;
	}
	public String getContact_confirm() {
		return contact_confirm;
	}
	public void setContact_confirm(String contact_confirm) {
		this.contact_confirm = contact_confirm;
	}
	
	
}
