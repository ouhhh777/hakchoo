package front.vo;

public class HakVO {

	private String hak_code;
	private String hak_latitude;
	private String hak_longitude;
	private String hak_name;

	private float swX;
	private float swY;
	private float neX;
	private float neY;
	
	
	
	public float getSwX() {
		return swX;
	}
	public void setSwX(float swX) {
		this.swX = swX;
	}
	public float getSwY() {
		return swY;
	}
	public void setSwY(float swY) {
		this.swY = swY;
	}
	public float getNeX() {
		return neX;
	}
	public void setNeX(float neX) {
		this.neX = neX;
	}
	public float getNeY() {
		return neY;
	}
	public void setNeY(float neY) {
		this.neY = neY;
	}
	public String getHak_code() {
		return hak_code;
	}
	public void setHak_code(String hak_code) {
		this.hak_code = hak_code;
	}
	public String getHak_latitude() {
		return hak_latitude;
	}
	public void setHak_latitude(String hak_latitude) {
		this.hak_latitude = hak_latitude;
	}
	public String getHak_longitude() {
		return hak_longitude;
	}
	public void setHak_longitude(String hak_longitude) {
		this.hak_longitude = hak_longitude;
	}
	public String getHak_name() {
		return hak_name;
	}
	public void setHak_name(String hak_name) {
		this.hak_name = hak_name;
	}
	
	
	
}
